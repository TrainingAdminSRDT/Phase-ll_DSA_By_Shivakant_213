a = 10
b = 3

# Arithmetic Operators
print("Arithmetic Operators")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)
print("a ** b =", a ** b)
print("a // b =", a // b)

# Comparison Operators
print("\nComparison Operators")
print("a == b :", a == b)
print("a != b :", a != b)
print("a > b :", a > b)
print("a < b :", a < b)
print("a >= b :", a >= b)
print("a <= b :", a <= b)

# Assignment Operators
print("\nAssignment Operators")
x = 5
x += 2
print("x += 2 :", x)
x -= 1
print("x -= 1 :", x)

# Logical Operators
print("\nLogical Operators")
print(a > 5 and b > 1)
print(a > 5 or b > 5)
print(not(a > 5))

# Bitwise Operators
print("\nBitwise Operators")
print("a & b =", a & b)
print("a | b =", a | b)
print("a ^ b =", a ^ b)
print("a << 1 =", a << 1)
print("a >> 1 =", a >> 1)

