# Arithematic operators
a = 10
b = 5
print(a + b) # 15
print(a - b) # 5
print(a * b) # 50
print(a / b) # 2.0
print(a // b) # 2
print(a % b) # 0
print(a ** b) # 100000

# Camparison operators
print(a == b) # False
print(a != b) # True
print(a > b) # True
print(a < b) # False
print(a >= b) # True
print(a <= b) # False

# Logical operators
x = True
y = False
print(x and y) # False
print(x or y) # True
print(not x) # False
print(not y) # True

# Assignment operators
c = 10  
c += 5  # c = c + 5
print(c)  # 15      
c -= 3  # c = c - 3
print(c)  # 12
c *= 2  # c = c * 2
print(c)  # 24
c /= 4  # c = c / 4
print(c)  # 6.0
c //= 2  # c = c // 2
print(c)  # 3.0
c %= 2  # c = c % 2
print(c)  # 1.0
c **= 3  # c = c ** 3
print(c)  # 1.0

# Bitwise operators
x = 5  # 0101 in binary 
y = 3  # 0011 in binary
print(x & y)  # 1 (0001 in binary)
print(x | y)  # 7 (0111 in binary)
print(x ^ y)  # 6 (0110 in binary)
print(~x)  # -6 (1010 in binary)
print(x << 1)  # 10 (1010 in binary)
print(x >> 1)  # 2 (0010 in binary)

